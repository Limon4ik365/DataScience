# Напишите программу компьютерной игры "Угадай число",
# в которой компьютер генерирует случайное целое число от 1 до 15,
# а пользователь должен его угадать.
# После каждой неудачной попытки пользователя, программа должна сообщать ему о том,
# больше это число, или меньше загаданного, а в случае угадывания - поздравлять с выигрышем.

import random

print('Угадайте число!')
random_number = random.randint(1, 15)
my_number = int(input('Отгадайте число от 1 до 15: '))

while (my_number != random_number):
    if my_number > random_number:
        print('Введите число меньше')
    elif my_number < random_number:
        print('Введите число больше')
    my_number = int(input('Отгадайте число от 1 до 15: '))
print('Вы угадали!')

