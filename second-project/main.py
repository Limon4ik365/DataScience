import re

text = '''
Имя: Иван, Группа: A123, Средний балл: 4.75
Имя: Петр, Группа: B456, Средний балл: 3.92
Имя: Елена, Группа: A123, Средний балл: 4.89
Имя: Анна, Группа: C789, Средний балл: 4.12
Имя: Михаил, Группа: B456, Средний балл: 5.01
Имя: Алиса, Группа: A123, Средний балл: 4.98
'''.strip()

with open('data.txt', 'w') as f:
    f.write(text)

with open('data.txt', 'r') as f:
    students = f.read()

name = re.findall(r'Имя: \w+', students)
group = re.findall(r'Группа: [A-Z]{1}\d{3}', students)
middle_score = re.findall(r'Средний балл: \d{1}\.\d+', students)

students_array = []

for i in range(len(name)):
    student = {
        'Имя': name[i],
        'Группа': group[i],
        'Средний балл': middle_score[i]
    }
    students_array.append(student)

for student in students_array:
    print(f"{student['Имя']}, \n {student['Группа']}, \n {student['Средний балл']}\n")